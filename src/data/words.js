// Función para obtener palabras aleatorias
export const getRandomWords = (category = 'all', count = 50) => {
  let words = [];
  
  if (category === 'all') {
    // Mezclar todas las categorías
    Object.values(wordCategories).forEach(cat => {
      words = [...words, ...cat];
    });
  } else {
    words = wordCategories[category] || [];
  }
  
  // Barajar las palabras
  const shuffled = words.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};
